package icons

type IconType byte

const (
	NONE IconType = iota
	INCREASE
	DECREASE
	PEN
	ERASER
	FILL
	RECT
	CIRC
	LINE
	SAVE
	OPENFILE

	BITMAPWIDTH = 12
)

type Icon struct {
	Data   *[]byte
	IcType IconType
}

func NewIcon(tp IconType) *Icon {
	var dt *[]byte
	switch tp {
	case INCREASE:
		dt = &INCREASEICON
	case DECREASE:
		dt = &DECREASEICON
	case ERASER:
		dt = &ERASERICON
	case FILL:
		dt = &FILLICON
	case PEN:
		dt = &PENICON
	case SAVE:
		dt = &SAVEICON
	case OPENFILE:
		dt = &OPENFILEICON
	case LINE:
		dt = &LINEICON
	case CIRC:
		dt = &CIRCLEICON
	case RECT:
		dt = &RECTICON
	}
	return &Icon{
		Data:   dt,
		IcType: tp,
	}
}

